def main():
    heads = json.load(open(os.path.join(HERE, "awl_headwords.json"), encoding="utf-8"))
    out = []
    missing = []
    for key in sorted(heads.keys(), key=lambda x: int(x.split("_")[1])):
        sub = int(key.split("_")[1])
        for word in heads[key]:
            if word not in DEFS:
                missing.append(word)
                continue
            pos, en, zh = DEFS[word]
            out.append({
                "word": word,
                "group": "Sublist %d" % sub,
                "primary": zh,
                "secondary": "%s %s" % (pos, en),
            })
    if missing:
        raise SystemExit("Missing definitions for: " + ", ".join(missing))
    dst_dir = os.path.join(HERE, "datasets", "awl")
    os.makedirs(dst_dir, exist_ok=True)
    dst = os.path.join(dst_dir, "words.json")
    json.dump(out, open(dst, "w", encoding="utf-8"), ensure_ascii=False, indent=1)
    print("Wrote %d words to %s" % (len(out), dst))


if __name__ == "__main__":
    main()
